package com.chicken.dropper.di

import com.chicken.dropper.data.repository.DefaultPlayerRepository
import com.chicken.dropper.domain.repository.PlayerRepository
import dagger.Binds
import dagger.Module
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
abstract class PlayerModule {

    @Binds
    @Singleton
    abstract fun bindPlayerRepository(impl: DefaultPlayerRepository): PlayerRepository
}
